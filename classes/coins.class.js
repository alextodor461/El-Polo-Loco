class Coin1 extends MovableObject{
    width = 100;
    height = 100;

    IMAGES_COIN =[
        'img/8.Coin/Moneda1.png',
        'img/8.Coin/Moneda2.png'
    ];
  
    constructor(){
        super().loadImage('img/8.Coin/Moneda1.png');
        this.loadImages(this.IMAGES_COIN);
        this.animates();
        this.x = 500;
        this.y = 300;
    }

    animates(){
        setInterval(() => {
           this.playAnimation(this.IMAGES_COIN);
        },300);   
    }   
}

